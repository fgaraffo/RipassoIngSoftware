package ripasso;

public class NuovaClasse {
	
	private int attributo;
	
	public NuovaClasse ()
	{
		setAttributo(0);		
	}
	public NuovaClasse (int attributo)
	{
		this.setAttributo(attributo);		
	}
	// GETTER SETTER se serve
	public int getAttributo() {
		return attributo;
	}
	public void setAttributo(int attributo) {
		this.attributo = attributo;
	}
	

}
